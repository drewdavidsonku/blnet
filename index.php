										<h2>About</h2>
									</header>
									<p>
The Black Literary Network consists of our four portals – the Novel
Generator Machine, the Literary Data Gallery, the Multithreaded
Literary Briefs, and the Remarkable Receptions podcast – designed to
make information and ideas about African American novels widely
available.
The portals for the Network address several basic yet enduring
questions: What’s a good African American novel to read?
What’s the story behind the publication of some famous novels by black
writers?
What’s the relationship of one novel to dozens of other novels? What
key terms assist us in understanding the works of black writers?
									</p>
									<p>
Funded by the Andrew W. Mellon Foundation and coordinated by the
Project on the History of Black Writing, a research unit in the
Department of English, the Network includes contributions by scholars
of African American literature, computer scientists, Digital Humanities
scholars, illustrators, graphic designers, voice actors, and sound
engineers.
The Network is a coordinated effort that puts to use decades of active
work identifying knowledge gaps and developing humanities resources for
research and public engagement.
